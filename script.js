const parseTimeToMinutes = (timeLabel) => {
  const time = safeText(timeLabel, "").toUpperCase();
  if (!time) {
    return Number.MAX_SAFE_INTEGER;
  }

  const hhmm24 = time.match(/^(\d{1,2}):(\d{2})$/);
  if (hhmm24) {
    return Number(hhmm24[1]) * 60 + Number(hhmm24[2]);
  }

  const ampm = time.match(/^(\d{1,2})(?::(\d{2}))?\s*(AM|PM)$/);
  if (ampm) {
    let hours = Number(ampm[1]) % 12;
    const minutes = Number(ampm[2] || "0");
    if (ampm[3] === "PM") {
      hours += 12;
    }
    return hours * 60 + minutes;
  }

  return Number.MAX_SAFE_INTEGER;
};

